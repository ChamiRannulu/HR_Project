    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        if (id.getText().equals("0")) {
            JOptionPane.showMessageDialog(this, "Please select row to delete");
        } else {
            if (JOptionPane.showConfirmDialog(null, "Are you sure you want to delete this?", "Delete Row", JOptionPane.YES_NO_OPTION) == 0) {
                SalaryShedule ss = new SalaryShedule();
                ss.setSalarySheduleId(Integer.parseInt(id.getText()));
                ss.setStaffId(Integer.parseInt(staff_id.getSelectedItem().toString()));
                ss.setBasicSalary(Double.parseDouble(basic_salary.getText()));
                ss.setOverTimeAmount(Double.parseDouble(over_time.getText()));
                ss.setAllowns(Double.parseDouble(allowns.getText()));

                SalarySheduleController.getInstance().deleteSalaryDetails(ss);
                table.removeAll();
                loadTableSedule();
                resetForm();
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

