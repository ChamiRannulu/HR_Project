    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed

        if (basic_salary.getText().isEmpty() || over_time.getText().isEmpty() || allowns.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please fill form");
        } else {
            SalaryShedule ss = new SalaryShedule();
            ss.setStaffId(Integer.parseInt(staff_id.getSelectedItem().toString()));
            ss.setBasicSalary(Double.parseDouble(basic_salary.getText()));
            ss.setOverTimeAmount(Double.parseDouble(over_time.getText()));
            ss.setAllowns(Double.parseDouble(allowns.getText()));

            SalarySheduleController.getInstance().addSalaryDetails(ss);
            table.removeAll();
            loadTableSedule();
            resetForm();
        }

    }//GEN-LAST:event_jButton2ActionPerformed

