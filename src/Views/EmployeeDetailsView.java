    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        if (id.getText().equals("0")) {
            JOptionPane.showMessageDialog(this, "Please select row to delete");

        } else {

            if (JOptionPane.showConfirmDialog(null, "Are you sure you want to delete this?", "Delete Row", JOptionPane.YES_NO_OPTION) == 0) {

                Staff s = new Staff();
                s.setEmployeeId(Integer.parseInt(id.getText()));
                s.setFname(fname.getText());
                s.setLname(lname.getText());
                s.setGender(gender.getSelectedItem().toString());
                s.setNo(address_no.getText());
                s.setStreet(address_street.getText());
                s.setCity(address_city.getText());
                s.setEmail(email.getText());
                s.setMobile(mobile.getText());
                s.setTelephone(telephone.getText());
                s.setDateOfJoin(dateofjoin.getText());
                s.setBasicSalary("");

                StaffController.getInstance().deleteStaffDetails(s);
                table.removeAll();
                loadTable();
                resetForm();
            }

        }
    }//GEN-LAST:event_jButton1ActionPerformed

