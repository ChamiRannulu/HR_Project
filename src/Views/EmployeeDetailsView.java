    private void serchDataFromTable(String s) {
        DefaultTableModel dtm = (DefaultTableModel) this.table.getModel();
        TableRowSorter<DefaultTableModel> trs = new TableRowSorter<>(dtm);
        table.setRowSorter(trs);
        trs.setRowFilter(RowFilter.regexFilter(s));
    }

    private void loadTable() {

        List<Staff> details = null;
        try {
            details = StaffController.getInstance().serchStaffDetails();

        } catch (Exception ex) {
//            Logger.getLogger(Home.class.getName()).log(Level.SEVERE, null, ex);
        }

        DefaultTableModel but_dt = (DefaultTableModel) this.table.getModel();
        but_dt.setRowCount(0);

        for (int i = 0; i < details.size(); i++) {
            Vector v = new Vector();

            Staff s = details.get(i);

            v.add(s.getEmployeeId());
            v.add(s.getFname());
            v.add(s.getLname());
            v.add(s.getGender());
            v.add(s.getNo());
            v.add(s.getStreet());
            v.add(s.getCity());
            v.add(s.getMobile());
            v.add(s.getTelephone());
            v.add(s.getEmail());
            v.add(s.getDateOfJoin());

            but_dt.addRow(v);

        }
    }

